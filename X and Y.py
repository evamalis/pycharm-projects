x = int(input('input x:'))
y = int(input('input y:'))
if x > 0 < y:               # x > 0, y > 0
         print("Первая четверть")
if x > 0 > y:                   # x > 0, y < 0
         print("Четвертая четверть")
if y > 0 > x:               # x < 0, y > 0
         print("Вторая четверть")
if y < 0 > x:                   # x < 0, y < 0
         print("Третья четверть")